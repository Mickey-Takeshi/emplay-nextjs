### Q. 無料版でも業務利用できますか？

A. 小規模な試用は可能ですが、法人運用では管理者権限、MFA、監査ログ、外部共有制御、退職者対応を確認します。個人向け無料版を全社運用へ流用せず、法人向けプランと比較してください。

