2. キャリアアップ計画と必要な事前手続きを確認する
3. 就業規則等へ正社員転換制度を定めて周知する
4. 対象労働者の雇用履歴と要件を確認する
5. 規定に基づく試験、面接などを実施する
6. 正社員への転換または直接雇用を行う
7. 転換後の雇用条件で勤務・賃金支払いを行う
8. 必要期間の賃金台帳、出勤簿などをそろえる
9. 申請期限までに管轄窓口へ支給申請する

