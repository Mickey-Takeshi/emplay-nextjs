## 申請前の社内準備

申請には、GビズID、決算情報、事業計画、見積書、登記事項、雇用関係書類などが必要になることがあります。まず公式公募要領で必要書類と締切を確認し、経営者、事業担当、経理、人事、外部支援者の役割を決めます。

担当者が一人で申請書を作ると、採択後に現場が計画を実行できないことがあります。申請内容、設備仕様、雇用条件、支払い時期、報告義務を関係者で共有してください。

支援会社へ依頼する場合も、採択保証や成功報酬だけで判断せず、契約範囲、申請主体、個人情報の取り扱い、採択後の報告支援、途中解約を確認します。申請者自身が内容を理解し、虚偽や誇張がないことを確認する責任は残ります。

## 採択・支給後まで見据える

制度の目的は、申請書を提出することではなく、計画した事業や雇用改善を実行することです。補助事業では、交付決定後の発注、納品、支払い、実績報告までを管理します。助成金では、対象労働者の雇用条件、賃金、勤務実績などを継続して確認します。

書類は申請時だけ集めるのではなく、証憑の保管場所、命名ルール、承認者、期限を決めます。担当者の異動や退職があっても追跡できる状態にしてください。

