### 過去と同じ制度が続いている

制度名、要件、公募主体は変わります。過去の採択事例は事業計画の参考にはなっても、現在の申請要件を示すものではありません。

### 外部支援者へ任せればよい

申請主体と事業実施の責任は申請者にあります。経営者と現場が内容を理解し、実行可能性と数字を確認してください。

