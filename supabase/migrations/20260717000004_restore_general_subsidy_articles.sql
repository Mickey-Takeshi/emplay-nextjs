## 経営計画を先に作る

採択のための表現を先に考えるのではなく、自社の現状、顧客、競合、強み、課題を整理します。そのうえで、誰に何を届け、どのような成果を目指すのかを一つの流れで説明します。

### 顧客を具体化する

「地域住民」「中小企業」のような広い表現だけでは、販路開拓の方法を決められません。利用場面、地域、業種、担当者、購入理由、比較対象を整理します。既存顧客と新規顧客で課題が違う場合は分けて考えます。

### 自社の強みを根拠とともに示す

品質が高い、対応が丁寧と書くだけでなく、リピート率、納期、資格、製造方法、顧客の評価、特定分野の経験など、確認できる事実を使います。競合を否定するのではなく、自社が選ばれる条件を説明します。

### 課題と施策をつなげる

例えば「若い顧客が少ない」という課題に対し、ホームページを作るだけでは十分ではありません。対象顧客がどこで情報を探し、何を比較し、どの行動へ進むのかを考え、ページ、広告、店頭、問い合わせ対応をつなげます。

計画には、売上目標だけでなく、来店数、問い合わせ数、商談数、客単価、リピート率など、取り組みとの関係を追える指標を置きます。数字の根拠には、過去実績、市場データ、見積もり、試験販売の結果を使ってください。

## 補助事業計画を具体化する

実施内容は「ホームページを作る」「広告を出す」「設備を買う」だけでは不十分です。対象顧客、訴求内容、実施時期、担当者、費用、期待する変化を具体化します。

施策ごとに次の項目を整理します。

| 項目 | 記載する内容 |
| --- | --- |
| 目的 | どの経営課題を解決するか |
| 対象 | 誰に届ける施策か |
| 内容 | 制作物、設備、広告、運用の具体的内容 |
| 担当 | 社内担当者と外注先の役割 |
| 時期 | 発注、制作、実施、検証の予定 |
| 費用 | 見積金額と経費区分 |
| 指標 | 問い合わせ、来店、売上などの測定方法 |

経営課題と施策のつながりが説明できるか、補助事業終了後も自社で継続できるかを確認しましょう。

## 見積もりと対象経費を確認する

同じ名称の費用でも、内容によって対象可否が異なることがあります。見積書には「一式」だけでなく、数量、単価、仕様、作業範囲を記載してもらいます。

ウェブサイト、広告、機械、展示会、外注などは、公募回によって条件や制限が設けられる場合があります。公募要領の経費区分と照らし合わせ、判断できない場合は事務局へ確認します。

補助対象外の費用を含めた総事業費も確認してください。消費税、振込手数料、保守費、更新費などを自社負担として見込む必要があります。

## 申請から実施までの流れ

1. 公式公募要領を確認する
2. GビズIDなど電子申請の準備をする
3. 経営計画と補助事業計画を作成する
4. 見積書など必要書類をそろえる
5. 商工会・商工会議所へ早めに相談する
6. 電子申請を行う
7. 採択後、交付申請と交付決定を確認する
8. 認められた内容と期間で事業を実施する
9. 証憑を保存し、期限内に実績報告を行う

採択は補助金の支給確定ではありません。交付申請や実績確認を経て、対象と認められた経費が確定します。交付決定前に発注、契約、支払いを行うと対象外になることがあるため、順序を守ります。

## 資金繰りと実施体制

補助金は後払いが基本となるため、外注費や設備費を一時的に自社で支払う資金が必要です。採択後に資金が足りないと事業を完了できません。自己資金、融資、支払条件を申請前に確認します。

また、計画書を作った担当者と、実際に施策を運用する担当者が異なる場合は、成果指標と報告方法を引き継ぎます。ウェブ制作なら公開後の更新、広告なら予算管理、設備なら操作教育と保守まで担当を決めます。

## よくある失敗

### 締切直前に相談する

商工会・商工会議所の確認期限が申請締切より前に設定される場合があります。計画の相談時間も含め、余裕を持って着手してください。

### 交付決定前に発注する

発注・契約・支払いの時期によって対象外になることがあります。必ず申請する回のルールを確認します。

### 経費の説明だけで終わる

審査では、何を買うかだけでなく、経営課題をどう解決し、販路開拓へつなげるかを示す必要があります。

### 数字の根拠がない

売上が倍になるといった結論だけでなく、顧客数、反応率、単価、実施回数から計算します。過度に楽観的な計画は実行時の資金不足にもつながります。

### 採択後に内容を大きく変える

外注先、仕様、金額、実施時期の変更には手続きが必要になる場合があります。変更前に手引きと事務局の案内を確認します。

