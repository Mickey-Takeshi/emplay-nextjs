2. **対象労働者**: 雇用期間、雇用形態、過去の雇用関係などの要件
3. **転換制度**: 就業規則等に定めた正社員転換制度と選考手続き
4. **実施後の処遇**: 賃金、所定労働時間、適用制度などの条件

