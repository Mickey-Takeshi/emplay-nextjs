## 転換前に準備すること

### キャリアアップ計画を確認する

制度では、取り組みを進める前に計画の作成や提出が必要になる場合があります。提出期限、計画期間、担当者、対象コースを公式資料で確認します。転換後に計画を作っても、要件を満たせない可能性があります。

### 就業規則に転換制度を定める

正社員への転換時期、試験または面接などの手続き、対象者の考え方を就業規則等へ定めます。実際の運用と規定が一致していることが重要です。

常時使用する労働者数によって就業規則の届出義務が異なりますが、助成金上どのような書類と手続きが必要かは別途確認します。規定を作成した日、労働者へ周知した日、労働基準監督署へ届け出た日を記録してください。

### 雇用契約と賃金を確認する

転換前後の雇用契約書または労働条件通知書を用意します。基本給、固定的手当、賞与、所定労働時間、休日、契約期間、社会保険の適用などを比較します。

賃金要件を確認するときは、総支給額だけでなく、どの手当を計算へ含めるか、時間外労働の増減が影響しないかを公式資料で確認します。転換後の賃金を実際に支払った記録も必要です。

### 対象労働者へ説明する

助成金のためだけに形式的な転換を行うのではなく、職務、責任、評価、賃金、勤務場所、転勤、研修、昇進を本人へ説明します。認識が違うと、転換後の早期離職や労務トラブルにつながります。

