- キャリアアップ計画に関する書類
- 転換制度を定めた就業規則等
- 転換前後の雇用契約書・労働条件通知書
- 選考、面接、本人通知の記録
- 賃金台帳と給与明細
- 出勤簿、タイムカード、勤怠データ
- 雇用保険、社会保険の届出記録
- 就業規則の届出と周知を確認できる記録

申請時だけ書類を作るのではなく、実際の勤務、支払い、社内規定と一致させます。電子データで管理する場合も、修正履歴と承認者を確認できる状態にします。

## よくある不支給リスク

### 事前手続きより先に転換した

計画や就業規則の整備前に転換すると、制度上の順序を満たせない可能性があります。対象者が決まった時点ではなく、制度導入を検討した時点で準備します。

### 就業規則と実際の選考が違う

就業規則に試験や面接を定めているのに記録がないなど、規定と運用の不一致は問題になります。選考日、評価者、結果を保存します。

### 賃金の計算方法を誤った

時間外手当や一時的な手当を含めて判断するなど、公式資料と異なる計算をすると要件を満たさない可能性があります。転換前に給与担当者と計算方法を確認します。

### 申請期限を転換日だけで管理した

申請期限は、転換後の賃金支払い期間や支給対象期などと関係します。対象者ごとに転換日、賃金締日、支払日、申請可能期間を管理します。

### 書類同士の内容が一致しない

雇用契約書、賃金台帳、出勤簿、雇用保険届出で、氏名、日付、勤務時間、賃金が一致するか確認します。差異には合理的な理由と記録が必要です。

## 助成金の有無とは別に正社員化を設計する

正社員化は、支給申請のための一時的な処遇変更ではありません。本人の役割、評価、育成、配置、賃金制度を継続的に運用する必要があります。

転換後のオンボーディング、業務目標、1on1、研修、キャリア面談を計画します。正社員になったことで責任だけが増え、支援が変わらなければ定着につながりません。

