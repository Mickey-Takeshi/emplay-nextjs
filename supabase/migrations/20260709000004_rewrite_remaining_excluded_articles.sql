### 雇用関係の助成金

雇用、教育訓練、仕事と家庭の両立などを目的とする助成金は、厚生労働省の制度から確認します。制度ごとに実施前の計画提出や就業規則などの要件があるため、施策を実施した後では申請できない場合があります。

## 制度を比較する5つの確認項目

1. **対象者**: 企業規模、業種、所在地などの条件を満たすか
2. **対象事業**: 実施したい事業が制度目的に合うか
3. **対象経費**: 発注予定の費用が対象になるか
4. **申請時期**: 交付決定前に契約・発注してよいか
5. **実施後の義務**: 実績報告、賃上げ、事業化報告などに対応できるか

金額が大きい制度を優先するのではなく、自社の事業計画と無理なく一致する制度を選びます。

