## 3つの違いを一文で整理

RAGは回答時に必要な情報を検索してモデルへ渡す仕組み、ファインチューニングは例示データでモデルの振る舞いを調整する方法、AIエージェントはモデルがツールを使いながら複数の手順を進めるシステムです。

| 観点 | RAG | ファインチューニング | AIエージェント |
| --- | --- | --- | --- |
| 主な目的 | 外部・社内情報を参照して回答 | 形式、文体、判断パターンを調整 | 目標に向けて手順とツールを実行 |
| 主な入力 | 検索対象の文書・データ | 品質の高い入出力例 | 指示、ツール、データ、業務ルール |
| 情報更新 | 文書や索引を更新 | 再学習が必要になる場合がある | 接続先やRAGの情報に依存 |
| 出典表示 | 設計しやすい | 学習例の出典提示には不向き | RAGやツール結果を記録できる |
| 主な難しさ | 検索品質とデータ整備 | 学習データ品質と評価 | 権限、例外、監視、実行リスク |

