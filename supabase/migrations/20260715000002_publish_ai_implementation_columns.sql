## RAGとは

RAGはRetrieval-Augmented Generationの略で、質問に関連する文書を検索し、その内容をモデルへ渡して回答を生成する仕組みです。モデルが事前に学習していない社内情報や、頻繁に更新される製品情報を扱うときに向いています。

### RAGが向く用途

- 社内規程やマニュアルの検索・回答
- 製品FAQやサポート回答の下書き
- 契約書、技術文書、議事録の横断検索
- 回答と一緒に根拠文書を示したい業務
- 情報を日次・週次で更新したい業務

RAGは文書を接続すれば自動的に正確になるわけではありません。古い版、重複、アクセス権、表や画像の読み取り、文書の分割方法が検索品質を左右します。詳しくは[RAGの精度を高めるデータ整備](/blog/rag-data-preparation-guide)で解説します。

### RAGが向かないケース

参照情報がなく、文章のトーンや出力形式だけを安定させたい場合は、RAGだけでは十分でないことがあります。また、検索した後に複数システムへ登録・送信する処理は、エージェントや通常のワークフローが必要です。

## ファインチューニングとは

ファインチューニングは、目的に合う入出力例を使って既存モデルを追加調整する方法です。特定形式の出力、分類、文体、専門的なタスクの再現性を高めたいときに検討します。

### ファインチューニングが向く用途

- 決まったラベルへの分類
- 自社で定義した文章形式への変換
- ブランドの文体や回答スタイルの安定
- プロンプトだけでは再現しにくい判断パターン
- 大量処理で指示文を短くしたいケース

学習データには、実際に望む品質の例が必要です。誤った回答、基準の揺れ、個人情報、利用権限のないデータが混ざると、期待する改善が得られません。学習用、検証用、最終評価用のデータを分けます。

### 最新知識の追加だけに使わない

頻繁に変わる料金、在庫、規程などを覚えさせる目的では、更新と出典確認が難しくなります。最新情報を回答時に参照させたい場合はRAGやAPI連携を優先します。

利用できるモデル、データ形式、保持方針はサービスによって変わります。実装時は、使用するサービスの公式ファインチューニング資料とデータ利用条件を確認してください。

## AIエージェントとは

AIエージェントは、目標に応じて情報を集め、手順を選び、外部ツールを利用し、結果を確認しながら処理を進めるシステムです。文章を返すだけでなく、CRM検索、予定確認、下書き保存などの行動を含められます。

### AIエージェントが向く用途

- 複数システムをまたぐ定型・半定型業務
- 問い合わせ内容に応じた調査と回答案作成
- 営業準備、記録、フォローアップの支援
- 監視結果に応じた調査と担当者への連絡
- 人の承認を含む複数ステップのワークフロー

エージェントは柔軟な一方、権限と例外処理が複雑になります。何を実行できるか、どの時点で人へ戻すか、失敗時にどう停止・復旧するかを要件として定義します。詳しくは[AIエージェント導入の要件定義](/blog/ai-agent-requirements-definition)を参照してください。

## 目的別の選び方

選定では「どの技術が高度か」ではなく、「業務のどこが不足しているか」を見ます。知識、振る舞い、実行のどれを補いたいかで、最初の候補を絞れます。

| 解決したいこと | 第一候補 | 理由 |
| --- | --- | --- |
| 最新の社内規程を根拠付きで回答 | RAG | 文書更新を回答へ反映しやすい |
| 指定されたJSONや分類形式で安定出力 | ファインチューニングまたは構造化出力 | 形式と判断例を学習・制約できる |
| 問い合わせを調査しCRMへ下書き登録 | AIエージェント＋RAG | 情報検索とツール実行が必要 |
| ブランド文体で大量の原稿案を作る | プロンプト、必要ならファインチューニング | 文体と形式の再現が中心 |
| 在庫を確認して回答する | API連携またはエージェント | 最新の業務データを直接取得する必要がある |

最初は、プロンプトと既存モデルで評価用ケースを作ります。それで不足する原因が「情報」ならRAG、「振る舞い」ならファインチューニング、「複数手順の実行」ならエージェントを検討すると、過剰な開発を避けられます。

## 組み合わせる代表パターン

実務では、単独より組み合わせが多くなります。ただし、構成要素を増やすほど評価と障害切り分けが難しくなるため、必要な理由を説明できるものだけ追加します。

### RAG＋AIエージェント

エージェントが社内文書をRAGで検索し、根拠を確認してから回答案を作り、人の承認後にチケットへ登録します。知識と実行の両方が必要な問い合わせ対応に向きます。

### ファインチューニング＋RAG

RAGで最新情報を取得し、ファインチューニングしたモデルが自社形式へ整えます。情報の鮮度と出力の一貫性を分けて改善できます。

### 通常ワークフロー＋一部AI

条件分岐と実行は従来のプログラムで固定し、分類や要約だけAIへ任せます。自由度の高いエージェントより、予測可能性を優先したい業務に適しています。

## 比較時に確認する6項目

技術名称ではなく、データと運用を含む総コストで比較します。試作費用が小さくても、文書更新や人の確認に多くの時間がかかる場合があります。

1. 情報はどのくらいの頻度で変わるか
2. 回答に根拠や出典が必要か
3. 望む入出力例を十分に用意できるか
4. 外部システムの読み書きが必要か
5. 誤りが起きたときの影響と人の確認方法
6. データ更新、評価、障害対応を誰が続けるか

NISTのAI RMFは、AIシステムのライフサイクルを通じてリスクを管理する考え方を示しています。[NIST AI RMF](https://www.nist.gov/itl/ai-risk-management-framework)を参照し、導入時だけでなく変更と廃止まで含めて検討してください。

## 評価は技術ごとに分ける

組み合わせたシステムの回答が悪いとき、検索、モデル、指示、ツール実行のどこに原因があるかを切り分けます。最終回答だけを採点すると、改善箇所が分かりません。

| 構成 | 主な評価 |
| --- | --- |
| RAG | 必要文書を取得できたか、不要文書が混ざらないか |
| ファインチューニング | 未学習の評価ケースで形式・判断を再現できるか |
| AIエージェント | 手順選択、ツール引数、完了、停止、人への引き継ぎ |
| 全体 | 正確性、安全性、処理時間、コスト、利用者の修正量 |

評価用ケースは、通常例だけでなく、情報がない質問、矛盾した文書、権限外の依頼、外部ツールのエラーを含めます。

## よくある質問

### RAGを使えばハルシネーションはなくなりますか？

なくなりません。関連文書を取得できない、文書が古い、取得内容を誤って解釈する可能性があります。根拠表示、回答不能の条件、人の確認を設計します。

### ファインチューニングで社内文書を覚えさせるべきですか？

頻繁に更新する事実情報ならRAGが適しています。ファインチューニングは、形式、文体、分類、判断パターンを安定させる用途から検討してください。

### AIエージェントには必ずRAGが必要ですか？

必要ではありません。外部ツールや構造化データだけで処理できる場合もあります。社内文書から情報を取得する必要があるときにRAGを組み合わせます。

### どれが最も安く導入できますか？

用途とデータで変わります。まず既存モデルとプロンプトで検証し、不足原因に必要な仕組みだけ追加する方法が、過剰投資を避けやすくなります。

### ノーコードでも実装できますか？

小規模な検証は可能です。ただし、権限、ログ、個人情報、例外処理、本番運用はツールの範囲を確認し、必要に応じて専門家のレビューを受けてください。

