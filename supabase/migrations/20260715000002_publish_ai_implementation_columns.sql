RAGは情報を参照する仕組み、ファインチューニングは振る舞いを調整する方法、AIエージェントは手順とツールを使って業務を進めるシステムです。知識、振る舞い、実行のどこに課題があるかを整理し、最小構成で評価してから組み合わせてください。

