## AI検索流入で測れるもの・測れないもの

AI検索の計測では、「AIがページを参照したこと」と「利用者がリンクをクリックしたこと」を分けます。GA4はページへ到達して計測タグが動いた訪問を記録するため、AI回答に引用されてもクリックがなければセッションとして確認できません。

| 確認したいこと | 主な方法 | 制約 |
| --- | --- | --- |
| ChatGPTなどからのクリック | GA4の参照元・UTM | 参照情報が送られない訪問は識別できない |
| Google AI Overviewからのクリック | GA4、Search Console | 通常のGoogleオーガニックと分離できない |
| AI回答での引用・言及 | 対象質問を実際に確認 | 回答は人・時点・条件で変わる |
| AIクローラーのアクセス | サーバーログ | 閲覧や引用、クリックを意味しない |
| 問い合わせ・売上 | GA4のキーイベント、CRM | 流入元が欠落する場合がある |

最初に「AI流入数を完全に把握する」ことを目標にすると、計測仕様の限界で行き詰まります。把握できるクリックと成果を継続し、引用確認は代表質問の定点観測として扱います。

## ChatGPT経由の流入をGA4で確認する

OpenAIは、ChatGPT検索結果からの参照URLに`utm_source=chatgpt.com`を付与すると案内しています。参照元情報が届いた訪問は、GA4の集客レポートで確認できます。公式情報は[OpenAIのパブリッシャー向けFAQ](https://help.openai.com/en/articles/12627856-publishers-and-developers-faq)を参照してください。

### 標準レポートで確認する手順

1. GA4の「レポート」を開く
2. 「集客」から「トラフィック獲得」を開く
3. 主ディメンションを「セッションの参照元 / メディア」にする
4. 検索欄で`chatgpt`、`openai`などを確認する
5. セッション、エンゲージメント、キーイベントを比較する

ランディングページも合わせて確認します。どの記事が入口になり、問い合わせ、資料閲覧、サービスページへの遷移につながったかを見ると、流入数の多寡より事業への貢献を判断しやすくなります。

## AIサービス用のカスタムチャネルを作る

複数のAIサービスから流入がある場合、GA4のカスタムチャネルグループでまとめると月次比較がしやすくなります。Googleも、AIアシスタントからの流入をまとめるカスタムチャネルの作成例を公開しています。

設定方法は[GA4カスタムチャネルグループの公式ヘルプ](https://support.google.com/analytics/answer/13051316)で確認できます。GA4の管理権限や画面構成は変更されることがあるため、実際の画面では公式手順を優先してください。

### 設定の考え方

1. 既存のデフォルトチャネルを複製する
2. 「AI Assistants」などの新しいチャネルを作る
3. ChatGPT、Perplexity、Claude、Gemini、Copilotなど対象参照元を条件にする
4. Referralより上へ配置する
5. テスト期間の参照元が意図どおり分類されるか確認する

参照元のドメインや表記は変わる可能性があります。正規表現を設定して終わりにせず、月次で未分類のReferralとDirectを確認し、必要な条件だけ追加します。`ai`のように短すぎる文字列で一致させると、無関係なドメインまで含む可能性があるため注意してください。

## Google AI Overviewの流入を確認する

Googleは、AI OverviewやAI Modeに表示されたリンクの実績をSearch Consoleの「ウェブ」検索へ含めると説明しています。ただし、AI機能だけを抽出する専用フィルタは提供されていません。GA4でもGoogle AI OverviewからのクリックはOrganic Searchとして扱われます。

Googleの現行仕様は[AI機能とウェブサイト](https://developers.google.com/search/docs/appearance/ai-features)で確認できます。

### 代わりに見る指標

- 複雑な質問形式のクエリで表示・クリックが増えたか
- 比較、方法、理由などのロングテールクエリが増えたか
- 対象記事の表示回数、平均掲載順位、CTRがどう変化したか
- Googleオーガニック流入後のエンゲージメントと問い合わせ
- 指名検索やサービス名を含む検索が増えたか

AI Overviewの表示有無だけで変化を説明しないでください。順位変動、季節性、記事更新、検索需要、競合の変化も同時に起きます。Search Consoleの注釈や更新履歴を残し、変更前後を比較します。

## Search Consoleで対象記事を追う

Search Consoleでは、記事群をページフィルタで絞り、クエリと成果を確認します。AI検索を意識した記事は、質問が多様になるため、上位数語だけでなく新規クエリとロングテールの増減を見ます。

### 月次で確認する項目

1. ページ別の表示回数、クリック、CTR、平均掲載順位
2. 新しく表示された質問形式のクエリ
3. 順位はあるがCTRが低いクエリ
4. 表示は増えたが問い合わせにつながらないページ
5. 更新後に失われたクエリと増えたクエリ

クエリの一部はプライバシー保護などの理由で表示されないため、Search Consoleの合計と一覧の合計が一致しない場合があります。見えているクエリだけを全体とみなさないでください。

## AIによる引用・言及を定点観測する

クリック計測だけでは、AI回答内で自社がどのように扱われているか分かりません。事業に関連する代表質問を10〜20個決め、月1回、同じ条件で回答と出典を確認します。

### 観測表に残す項目

| 項目 | 記録内容 |
| --- | --- |
| 質問 | 実際に入力した全文 |
| AIサービス | ChatGPT、Google、Perplexityなど |
| 確認日 | 回答は変わるため必須 |
| 自社の言及 | 社名・サービス名・内容 |
| 出典URL | 自社ページがリンクされたか |
| 表現 | 正確、中立、古い、誤りなど |
| 競合 | 同時に示された企業・記事 |
| 改善案 | 不足している一次情報や説明 |

一度の回答で採用・不採用を判断しません。AI回答は利用者、場所、会話、時点によって変わります。固定した質問で傾向を見ながら、誤情報がある場合は自社サイトの公式情報を明確にします。

