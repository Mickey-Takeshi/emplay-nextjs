## 制作会社を比較する評価表

価格だけで選ばず、目的理解と実行体制を含めて評価します。評価項目へ重みを付け、選定メンバーが個別に採点してから議論すると、声の大きい人だけで決まりにくくなります。

| 評価項目 | 確認すること |
| --- | --- |
| 課題理解 | 背景と対象顧客を理解しているか |
| 戦略・情報設計 | KPIと導線に根拠があるか |
| コンテンツ | 原稿・取材・写真の分担が明確か |
| 技術・品質 | CMS、速度、セキュリティ、移行を説明できるか |
| 体制 | 実際の担当者と責任者が分かるか |
| 運用 | 公開後の更新・保守・改善が現実的か |
| 費用 | 範囲、単価、追加条件、保守費が明確か |

提案書の見栄えより、質問への回答、前提の明示、リスクの説明、契約後に担当する体制を確認します。

## よくある失敗

### デザイン要望から書き始める

目的と対象者が決まらないまま色や動きを指定すると、見た目は変わっても課題が残ります。背景、目的、KPI、利用者を先に書きます。

### 原稿と写真の担当が曖昧

制作の遅延原因になりやすい項目です。誰が素材を用意し、制作会社がどこまで取材・編集するかを見積もり前に明確にします。

### SEO移行が見積もりにない

URL対応表、リダイレクト、canonical、サイトマップ、公開後監視を要件へ入れます。詳しくは[サイトリニューアルでSEO順位を落とさない移行手順](/blog/website-renewal-seo-migration)を参照してください。

### 提案範囲が各社で違う

一社は撮影込み、別の会社はデザインだけという状態では総額を比較できません。見積もり項目と除外事項をそろえます。

## よくある質問

### RFPは何ページ必要ですか？

ページ数より、比較に必要な前提がそろっていることが重要です。小規模サイトなら5〜10ページ程度でも十分ですが、添付資料としてURL一覧や機能表を分けると読みやすくなります。

### 予算は制作会社へ伝えるべきですか？

伝えることを推奨します。上限と優先順位が分かれば、現実的な範囲と段階案を提案しやすくなります。

### RFP作成から制作会社へ相談できますか？

可能です。ただし、その会社が制作も受注する場合は、要件が自社の得意な方法へ偏らないか確認し、必要に応じて第三者レビューを受けます。

### 何社へ提案を依頼すればよいですか？

比較可能な候補を2〜3社に絞る方法が一般的です。多すぎると質疑と評価の負担が増えます。実績、対応範囲、予算感で事前選定してください。

### 契約前にデザイン案を依頼すべきですか？

無償の完成デザインより、課題理解、情報設計、進め方、担当体制を確認します。デザインの品質は関連実績と有償の初期設計で評価するほうが現実的です。

