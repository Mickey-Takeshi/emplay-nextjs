アンカーテキストには「詳しくはこちら」ではなく、リンク先の主題を書きます。リダイレクトを挟む旧URLや、内容が重複する複数記事へ分散してリンクしないようにします。

## ChatGPT検索で確認する技術項目

OpenAIは、公開サイトがChatGPT検索に表示・引用されるために、OAI-SearchBotをブロックしないよう案内しています。GPTBotは学習に関するクローラーであり、検索用のOAI-SearchBotとは役割が異なります。

詳細は[OpenAIのパブリッシャー向けFAQ](https://help.openai.com/en/articles/12627856-publishers-and-developers-faq)を確認してください。クローラーを許可しても掲載は保証されません。また、アクセス制御を変更する際は、セキュリティやコンテンツ利用方針と合わせて判断します。

### robots.txtで確認すること

- OAI-SearchBotを意図せず拒否していないか
- Googlebotを拒否していないか
- CDNやWAFでbotアクセスを一律遮断していないか
- `noindex`とrobots.txtの役割を混同していないか
- 本番と検証環境で設定が入れ替わっていないか

## FAQは実際の疑問に答える

FAQは検索語を増やすためではなく、本文を読んだ後に残る判断や例外へ答えるために使います。質問文の直後に結論を置き、その後に条件を説明します。

Googleの一部のリッチリザルト表示は対象サイトや条件が限定されるため、FAQ構造化データを付ければ目立つ表示になるとは限りません。画面に見えないFAQをマークアップしたり、本文と異なる内容を構造化データへ入れたりしないでください。

