## 要件定義書とRFPの違い

要件定義書は、制作するサイトが満たす条件を整理する文書です。RFPは、外部の制作会社へ背景と条件を伝え、提案や見積もりを依頼する文書です。実務では内容が重なるため、発注前は一つのRFPにまとめても問題ありません。

| 文書 | 主な目的 | 作成時期 |
| --- | --- | --- |
| 企画書 | なぜ制作・改修するか社内合意を取る | 予算化前 |
| RFP | 同じ条件で提案と見積もりを依頼する | 制作会社選定前 |
| 要件定義書 | 作る範囲と受け入れ条件を確定する | 選定前〜契約後 |
| 仕様書 | 実装の具体的な方法を定める | 設計・開発時 |

RFPを細かく固定しすぎると、制作会社の提案余地がなくなります。一方、目的だけを伝えて丸投げすると、各社の前提が異なり、金額と内容を比較できません。「必須」「希望」「提案希望」に分けて記載します。

## 作成前に現状を棚卸しする

新しいサイトの希望を書く前に、現在のサイトと業務の状況を確認します。アクセスや問い合わせのデータを見ずに全面改修すると、成果を出しているページや検索評価を失う可能性があります。

### 集める資料

- 現行サイトの全URLとページ構成
- Search Consoleの検索流入上位ページ・クエリ
- GA4の入口ページと問い合わせ経路
- 問い合わせ内容、件数、商談化状況
- 営業や採用でよく説明する質問
- 既存の原稿、写真、動画、ロゴ、ブランド資料
- 利用中のドメイン、サーバー、CMS、外部サービス
- 更新担当者と現在の運用工数

数字が取れていない場合は、その事実も要件へ書きます。公開時にGA4、Search Console、フォーム計測を整える必要があるからです。

## 要件1：背景・目的・KPI

最初に「古いからリニューアルする」ではなく、事業上の課題を説明します。問い合わせが少ない、サービスの違いが伝わらない、採用候補者が必要情報を見つけられない、更新に時間がかかるなど、現状と目標を分けます。

### 記載例

```text
背景：サービスが増えたが、トップページから違いが伝わらず、
      営業担当が毎回口頭で説明している。

目的：対象顧客が自社に合うサービスを判断し、相談まで進めるサイトにする。

主要KPI：有効問い合わせ数、サービスページからのフォーム到達率。
補助KPI：対象サービスページの閲覧、資料閲覧、指名検索。
```

PVだけを主要KPIにすると、事業成果と結びつかないアクセスを増やす提案になりやすくなります。問い合わせ、応募、購入など目的に近い指標を置きます。

## 要件2：対象者と利用場面

「30〜50代の会社員」のような属性だけでなく、どの状況で、何を知り、何を不安に感じ、誰と相談して決めるかを書きます。BtoBサイトでは利用者、担当者、上司、決裁者が別の場合があります。

| 対象者 | 知りたいこと | 次の行動 |
| --- | --- | --- |
| 初めて課題を調べる担当者 | 解決方法、用語、選択肢 | 関連記事・サービスを見る |
| 比較中の担当者 | 特徴、範囲、料金、実績 | 社内共有・相談する |
| 決裁者 | 信頼性、費用対効果、リスク | 問い合わせを承認する |
| 既存顧客 | 問い合わせ先、サポート | 担当窓口へ連絡する |

制作会社へペルソナ作成を依頼する場合も、実際の顧客質問、商談記録、検索データなど利用できる材料を共有します。

## 要件3：ページとコンテンツ

必要ページを一覧にし、各ページの目的、対象者、主要メッセージ、CTA、原稿担当を記載します。ページ名だけでは、見積もりに原稿制作や取材が含まれるか判断できません。

### ページ要件表のテンプレート

| ページ | 目的 | 主な内容 | CTA | 原稿・素材 | 優先度 |
| --- | --- | --- | --- | --- | --- |
| トップ | 事業と強みを短時間で伝える | 対象課題、サービス、実績 | サービス詳細 | 共同制作 | 必須 |
| サービス | 適合性を判断してもらう | 対象、内容、流れ、FAQ | 相談 | 自社＋取材 | 必須 |
| 事例 | 導入後を具体化する | 課題、支援、成果、条件 | 関連サービス | 取材希望 | 希望 |
| 会社情報 | 信頼性を確認できる | 会社概要、代表、所在地 | 問い合わせ | 自社提供 | 必須 |

記事やお知らせを設置する場合は、公開後に誰がどの頻度で更新するかも要件にします。更新できないコンテンツを増やすと、古い情報が残ります。

## 要件4：機能と外部連携

「お問い合わせフォーム」のような機能名だけでなく、入力項目、通知先、自動返信、同意、データ保存、スパム対策、完了後の計測まで記載します。外部サービスとの連携は、契約主体と費用も確認します。

### よくある機能

- お問い合わせ・資料請求フォーム
- CMSによる記事・実績・お知らせ更新
- サイト内検索
- 多言語表示
- CRM・MA・メール配信連携
- 予約、決済、会員機能
- アクセス解析と広告計測
- Cookie同意管理
- 採用管理システムへの連携

各機能を「必須」「希望」「将来」に分けます。公開時に不要な機能を詰め込むより、将来追加できる設計とするほうが、予算とスケジュールを管理しやすくなります。

## 要件5：デザインとアクセシビリティ

「信頼感のあるデザイン」だけでは解釈が分かれます。ブランドの印象、避けたい表現、参考サイトのどこを評価しているか、写真の方向性、既存ガイドラインを共有します。

### 伝える内容

- ブランドが約束する価値と顧客に与えたい印象
- ロゴ、色、フォント、写真などの既存ルール
- 参考サイトと評価する具体的な点
- 使用したくない表現や素材
- スマートフォンで優先する操作
- 文字、色、キーボード操作などアクセシビリティ要件

参考サイトを「このまま再現してほしい」と依頼せず、情報の見つけやすさ、余白、写真、導線など、参考にする理由を書きます。

## 要件6：SEO・AIO・計測

SEO要件には、タイトルやキーワードだけでなく、URL、リダイレクト、canonical、構造化データ、サイトマップ、表示速度、内部リンク、公開後の計測を含めます。リニューアルでは現行評価を引き継ぐ作業が特に重要です。

### 必須項目の例

- 現行URLと新URLの対応表
- 301リダイレクトの実装とテスト
- 自己参照canonicalとXMLサイトマップ
- robots.txtとnoindexの公開時確認
- GA4、Search Console、タグ管理
- フォーム開始・完了などキーイベント
- Article、Organizationなど表示内容と一致する構造化データ
- OAI-SearchBotなど検索用途のクローラー方針
- 画像の代替テキストと表示速度

AI検索についても、特殊なファイルの導入を成果とせず、重要情報をHTMLの本文で提供し、一次情報、著者、更新日、内部リンクを整える要件にします。

## 要件7：CMS・保守・運用

公開後に誰が更新するかで、必要なCMSと管理画面が変わります。更新頻度、担当者の知識、承認フロー、権限、バックアップ、セキュリティ更新を記載します。

### 運用要件の例

- 更新するコンテンツと頻度
- 投稿、編集、承認、公開の権限
- 操作マニュアルと研修
- バックアップと復旧
- CMS・プラグイン・依存ライブラリの更新
- 障害時の連絡先と対応時間
- 月次レポートと改善提案の範囲
- 契約終了時のデータとアカウント引き渡し

保守費用を比べるときは、作業時間だけでなく、何が含まれ、何が別料金かを確認します。

## 要件8：予算・スケジュール・契約条件

予算を非公開にすると、実現可能性が異なる提案が集まり、比較に時間がかかります。上限、希望、提案による調整余地を示し、見積もりの前提と内訳を依頼します。

### 見積もりで分ける項目

- 企画・要件定義
- 情報設計・ワイヤーフレーム
- デザイン
- 原稿、取材、撮影、素材
- フロントエンド・CMS実装
- フォーム・外部連携
- データ・コンテンツ移行
- SEO移行・リダイレクト
- テスト・公開
- 操作説明・保守・改善

スケジュールには、制作会社の作業だけでなく、自社の原稿提供、確認、法務審査、決裁期間を含めます。公開希望日が固定なら、その理由と動かせない日を明記します。

