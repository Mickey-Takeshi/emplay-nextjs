## よくある誤解

### AIクローラーが来れば引用されている

クローラーのアクセスは、ページを取得したことを示すだけです。回答での利用、引用、クリックを意味しません。サーバーログは発見性の診断に使い、成果指標とは分けます。

### DirectはすべてAI流入である

Directには、ブックマーク、URL直接入力、参照情報が欠落した訪問などが含まれます。増加分をすべてAI由来と判断できません。

### 引用数が増えれば問い合わせも増える

引用されても回答内で完結し、クリックされない場合があります。引用される質問が自社サービスと関連するか、ページに次の行動があるかも確認します。

### 専用ツールなら完全に測定できる

外部ツールも、取得できる回答、検索環境、クローラー、参照情報に制約があります。ツールの対象AI、確認頻度、地域、ログ、推定方法を確認してください。

## よくある質問

### ChatGPTからの流入はGA4のどこに出ますか？

参照元情報が届けば、トラフィック獲得の参照元 / メディアに`chatgpt.com`などとして表示されます。UTM付きの場合も同じ画面で確認できます。

### AI Overviewだけのクリック数を確認できますか？

現時点では専用の区分で確認できません。Search Consoleのウェブ検索とGA4のGoogleオーガニックに含まれます。

### AI流入が0なら対策は不要ですか？

0が「利用されていない」とは限りません。クリックがない、参照情報が欠落している、回答内で完結している可能性があります。検索需要と事業への関連性を基に施策を判断します。

### UTMは自社で付ける必要がありますか？

外部のAIサービスが付与するUTMは自社で制御できません。自社から配布するリンクにはUTMを付けられますが、AI回答内のリンクを改変することはできません。

### 何カ月で評価すべきですか？

少なくとも3カ月は同じ定義で追い、記事公開・更新日を記録します。流入が少ない場合は月次の増減率より、四半期のセッション、問い合わせ、引用傾向を確認します。

