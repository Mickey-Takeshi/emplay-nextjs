## AI活用度診断とは

AI活用度診断とは、社員と組織が生成AIをどの程度理解し、実務へ適用し、安全かつ継続的に利用できているかを確認する取り組みです。目的は社員を順位づけることではなく、学習や環境整備が必要な場所を特定することにあります。

診断は次の4領域で行います。

| 領域 | 確認すること | 代表的な問い |
| --- | --- | --- |
| スキル | 特性を理解し適切に指示・検証できるか | 出力の誤りを確認できるか |
| 業務適用 | 自分の業務で価値を出せているか | 継続利用する具体的な業務があるか |
| 安全性 | 情報や権利を守って利用できるか | 入力禁止情報を判断できるか |
| 組織支援 | ルール・環境・相談先があるか | 承認済みツールと相談先を知っているか |

4領域の平均だけを見ると、弱点が隠れます。たとえばスキルが高くても、承認済みツールや利用ルールがなければ業務利用は進みません。領域ごとの点数と自由記述を合わせて読みます。

## 診断前に決める3つのこと

質問を作る前に、診断の目的、対象、結果の使い方を決めます。目的が曖昧なまま実施すると、回答を集めても「利用者が何人いた」という報告で終わり、施策へつながりません。

### 1. 診断の目的

全社員研修の難易度を決めるのか、推進人材の候補を見つけるのか、導入後の定着を測るのかを一つ選びます。複数の目的がある場合も、回答者に示す主目的は明確にしてください。

### 2. 対象者の分け方

部署、職種、役職、利用ツール、利用経験で集計できるようにします。ただし、個人が特定される小さな部署では、自由記述の扱いと閲覧権限に配慮します。

### 3. 結果の利用範囲

育成目的か人事評価かで、回答者の行動は大きく変わります。導入初期は育成目的と明記し、個人の評価に直接使用しないほうが、つまずきや不安を正直に集めやすくなります。

## そのまま使える診断項目

回答は「まったく当てはまらない」から「自力で実行でき、他者へ説明できる」までの5段階にします。知識の有無ではなく、どの程度行動できるかが分かる表現にすると、研修内容を決めやすくなります。

### スキル領域

1. 生成AIが回答を生成する基本的な仕組みと限界を説明できる
2. 目的、前提、出力形式を含む指示を作れる
3. 回答の事実、計算、参照元を確認できる
4. 期待する回答が出ないとき、指示や情報を改善できる
5. 生成AIを使うべきでない業務を判断できる

### 業務適用領域

6. 自分の業務で使える具体的な場面を3つ以上挙げられる
7. 生成AIを使う前後の手順と、人が判断する箇所を整理できる
8. 継続的に利用している業務がある
9. 利用による時間、品質、処理件数などの変化を説明できる
10. 活用方法を同僚へ共有したことがある

### 安全性領域

11. 個人情報、機密情報、営業秘密の入力可否を判断できる
12. 著作権や第三者の権利を確認して出力を利用できる
13. 誤情報や偏りが生じる可能性を理解している
14. 重要な判断をAIだけに任せず、人が確認する範囲を決められる
15. 問題が起きた場合の報告先と記録方法を知っている

### 組織支援領域

16. 会社が利用を認めているAIサービスを知っている
17. 社内ルールやガイドラインを確認できる場所を知っている
18. 業務に使えるプロンプト例やテンプレートへアクセスできる
19. 困ったときに相談できる担当者や場がある
20. 上司がAI活用の試行と学習を支援している

組織支援の質問は、社員の能力を測るものではありません。点数が低い場合は、研修を増やすより先に、アカウント、ルール、相談先、上司の理解を整える必要があります。

## 自己評価を実技で補う

自己評価は短時間で全社の傾向を把握できますが、経験が少ない人ほど自分の不足に気づきにくく、経験者ほど慎重に低く評価することがあります。診断の重要な場面では、短い実技課題と知識確認を組み合わせます。

### 実技課題の例

「架空の顧客アンケート10件を要約し、改善案を3つ出す」という課題を用意します。その際、入力してよい情報、期待する形式、出力の確認方法も回答してもらいます。

評価は次の5項目で行います。

- 課題の目的を正しく捉えている
- 必要な前提と制約を指示している
- 個人情報や機密情報を除いている
- 出力の根拠と不足を確認している
- 最終判断を人が行っている

文章表現の巧みさだけを採点すると、業務で必要な安全性や検証力を見落とします。実際の職務に近い題材を使い、完成品だけでなく考え方を確認してください。

## 診断結果の採点と読み方

各質問を1〜5点で集計し、4領域それぞれの平均を出します。総合点は参考値とし、最も低い領域と、部署間の差、自由記述で繰り返される課題を優先します。

| 状態 | 点数の目安 | 次の施策 |
| --- | --- | --- |
| 未整備 | 1.0〜1.9 | 利用方針、承認ツール、基礎教育を整える |
| 試行 | 2.0〜2.9 | 業務別演習、相談会、テンプレートを用意する |
| 実践 | 3.0〜3.9 | 成果測定、事例共有、推進者育成を進める |
| 定着 | 4.0〜5.0 | 高度活用、標準化、システム連携を検討する |

この区分は自社で会話を始めるための目安であり、業界共通の認定基準ではありません。高リスク業務では、安全性が平均3点未満なら利用範囲を広げないなど、自社のリスクに合わせた最低条件を別途決めます。

## 結果から施策を決める方法

診断後は、点数を報告するだけで終わらせず、領域ごとに次の90日で行う施策と責任者を決めます。改善対象を一度に増やしすぎず、利用者が多い業務か、リスクが高い領域から優先します。

### スキルが低い場合

基本特性、指示の作り方、出力検証を扱う基礎研修を行います。一般論だけでなく、自社の業務文書を匿名化した演習を使います。

### 業務適用が低い場合

部署ごとに業務を棚卸しし、頻度、工数、定型性、リスクで候補を評価します。小さな業務を一つ選び、利用前後の手順と成果指標を決めて試します。

### 安全性が低い場合

利用を急いで広げず、入力禁止情報、利用可能なサービス、人の確認、事故時の報告を具体化します。経済産業省の[AI事業者ガイドライン](https://www.meti.go.jp/shingikai/mono_info_service/ai_shakai_jisso/)も確認してください。

### 組織支援が低い場合

研修の追加だけでは解決しません。アカウント、社内ルール、FAQ、相談窓口、管理職向け説明を整えます。推進担当が質問と事例を集め、月次で教材やルールを更新します。

## 診断を継続するタイミング

初回診断は研修前に行い、同じ中核質問を30日後と90日後に再確認します。すべての質問を毎月繰り返す必要はありません。利用率、安全理解度、継続利用、組織支援など5〜8項目に絞り、四半期ごとに全体診断を行う方法が現実的です。

IPAの[デジタルスキル標準Ver.2.0](https://www.ipa.go.jp/jinzai/skill-standard/dss/download.html)は、必要な役割とスキルを整理する参照枠として使えます。診断項目を見直す際は、社内の利用状況だけでなく、公式ガイドラインやツールの変化も確認してください。

## よくある質問

### アンケートは匿名で実施すべきですか？

組織課題を把握する初回診断は匿名が回答を得やすくなります。個別研修のフォローが目的なら記名とし、閲覧者と利用目的を明示します。

### 利用率はどのように定義しますか？

「過去30日以内に承認済みAIを業務で1回以上利用した人」など、期間、ツール、業務利用を明示します。単にアカウントへログインした人数とは分けます。

### 診断結果を人事評価へ使えますか？

導入初期は推奨しません。失敗や不安が共有されにくくなるためです。評価へ使用する場合は、業務要件、採点方法、再評価の機会を明示します。

### 全社員に実技課題が必要ですか？

全員に必須ではありません。推進候補、重要業務の担当者、研修効果を確認する代表サンプルへ実施すると負担を抑えられます。

### 点数が高ければAI導入を拡大してよいですか？

点数だけでは判断できません。対象業務のリスク、データ、権限、AI出力の評価、人の確認、事故対応を確認したうえで段階的に広げます。

## まとめ

社内のAI活用度は、スキル、業務適用、安全性、組織支援の4領域で診断します。自己評価だけでなく、実技、利用ログ、業務指標を組み合わせ、領域ごとの弱点から次の90日の施策を決めてください。

診断結果に合わせた研修や職種別カリキュラムを設計する場合は、[EMPLAY AI ACADEMY](https://academy.emplay.jp/)をご確認ください。研修前の現在地整理から、実践演習、受講後の定着確認まで一貫して検討できます。

