### Step 1: 事業用口座を登録する

銀行、クレジットカード、現金など、事業で使う支払手段をfreee上の「口座」として登録します。私用と事業用が混在すると確認が複雑になるため、原則として事業専用口座を使います。

### Step 2: 事業所・会計期間・税区分を確認する

事業者名、会計期間、消費税の扱いなどを設定します。個別の税務判断が必要な項目は、前年データや届出書を確認し、推測で選ばないでください。

### Step 3: 開始残高を設定する

期首時点の預金、売掛金、未払金、借入金などを登録します。期中から移行する場合や他社会計ソフトから乗り換える場合は、決算書・試算表・移行データと一致するかを確認します。

### Step 4: 勘定科目と取引先ルールを揃える

既存の科目を確認し、必要な科目だけを追加します。同じ支出を担当者ごとに別の科目へ登録しないよう、よく使う取引のルールを決めます。

### Step 5: 口座同期を設定する

オンラインバンキングやカードを連携し、明細を取り込みます。同期しただけでは取引登録が完了しないため、「自動で経理」で内容を確認して登録します。

